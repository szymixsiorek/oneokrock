// Helper function to look up duration by track title
export function getTrackDuration(title: string): string | null {
  const normalizedTitle = title.toLowerCase()
    .replace(/\.m4a$|\.mp3$|\.wav$|\.flac$/i, '')
    .replace(/^\d+[\.\s-]+/, '') // Remove track number prefix
    .trim();
  
  return trackDurations[normalizedTitle] || null;
}
